package com.jebussystems.levelingglass.control.config;

import com.jebussystems.levelingglass.control.MeterType;

public abstract class MeterConfig implements Comparable<MeterConfig>
{
	// /////////////////////////////////////////////////////////////////////////
	// constants
	// /////////////////////////////////////////////////////////////////////////

	private int channel;

	// /////////////////////////////////////////////////////////////////////////
	// types
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// class variables
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// object variables
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// static initialization
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// constructors
	// /////////////////////////////////////////////////////////////////////////

	protected MeterConfig(int channel)
	{
		this.channel = channel;
	}

	protected MeterConfig()
	{
	}

	// /////////////////////////////////////////////////////////////////////////
	// Comparable implementation
	// /////////////////////////////////////////////////////////////////////////
	
	public int compareTo(MeterConfig value)
	{
		return value.channel - channel;
	}
	
	// /////////////////////////////////////////////////////////////////////////
	// public methods
	// /////////////////////////////////////////////////////////////////////////

	public int getChannel()
	{
		return channel;
	}
	
	public void setChannel(int channel)
	{
		this.channel = channel;
	}

	public abstract MeterType getMeterType();

}

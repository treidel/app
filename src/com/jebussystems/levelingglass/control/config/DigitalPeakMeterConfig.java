package com.jebussystems.levelingglass.control.config;

import com.jebussystems.levelingglass.control.MeterType;

public class DigitalPeakMeterConfig extends MeterConfig implements
        HoldTimeConfig
{
	// /////////////////////////////////////////////////////////////////////////
	// constants
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// types
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// class variables
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// object variables
	// /////////////////////////////////////////////////////////////////////////

	private Integer holdtime;

	// /////////////////////////////////////////////////////////////////////////
	// static initialization
	// /////////////////////////////////////////////////////////////////////////

	// /////////////////////////////////////////////////////////////////////////
	// constructors
	// /////////////////////////////////////////////////////////////////////////

	DigitalPeakMeterConfig(int channel)
	{
		super(channel);
	}

	protected DigitalPeakMeterConfig()
	{

	}

	// /////////////////////////////////////////////////////////////////////////
	// MeterConfig implementation
	// /////////////////////////////////////////////////////////////////////////

	@Override
	public MeterType getMeterType()
	{
		return MeterType.DIGITALPEAK;
	}

	// /////////////////////////////////////////////////////////////////////////
	// HoldTimeConfig implementation
	// /////////////////////////////////////////////////////////////////////////

	@Override
	public Integer getHoldtime()
	{
		return holdtime;
	}

	@Override
	public void setHoldtime(Integer holdtime)
	{
		this.holdtime = holdtime;
	}

}
